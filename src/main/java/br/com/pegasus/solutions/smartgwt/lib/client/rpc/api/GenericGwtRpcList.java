package br.com.pegasus.solutions.smartgwt.lib.client.rpc.api;

import java.util.ArrayList;

public class GenericGwtRpcList<D> extends ArrayList<D> {
	private static final long serialVersionUID = 6325861983162367825L;
	private Integer totalRows;
	private Integer pageSize;
	private Integer currenPage;

	public GenericGwtRpcList() {
	}

	public Integer getTotalRows() {
		return this.totalRows;
	}

	public void setTotalRows(Integer totalRows) {
		this.totalRows = totalRows;
	}

	public Integer getCurrenPage() {
		return this.currenPage;
	}

	public void setCurrenPage(Integer currenPage) {
		this.currenPage = currenPage;
	}

	public Integer getPageSize() {
		return this.pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

}